package humanainet.ui.core.shapes.withcolor;

public class DrawCircle{
	
	public final float y, x, radius;
	
	public final int colorARGB;
	
	public DrawCircle(float y, float x, float radius, int colorARGB){
		this.y = y;
		this.x = x;
		this.radius = radius;
		this.colorARGB = colorARGB;
	}

}